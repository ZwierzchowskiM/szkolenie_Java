package exc1;

public class Calc {

    int a;
    int c;

    double add (double a, double b){
        double c = a + b;
        return (c);
    }

    double subtract (double a, double b){
        return (a-b);
    }

    double mul (double a, double b){
        return (a*b);
    }

    double div (double a, double b){
        return (a/b);
    }

}
